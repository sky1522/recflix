// ===== 헬퍼 함수 =====

// 랜덤 선택 헬퍼
export function getSubtitle(arr: string[] | undefined, index: number): string {
  if (!arr || arr.length === 0) return '';
  return arr[index % arr.length];
}

// 맞춤 추천 섹션: 시간대 문구
export function getHybridContextSubtitle(
  timeOfDay: string,
  index: number
): string {
  return getSubtitle(TIME_SUBTITLES[timeOfDay], index);
}

// 날씨 섹션: 계절 + 기온 교대
// 짝수 인덱스면 계절 문구, 홀수 인덱스면 기온 문구
export function getWeatherContextSubtitle(
  season: string,
  tempRange: string,
  index: number
): string {
  if (index % 2 === 0) {
    return getSubtitle(SEASON_SUBTITLES[season], index);
  } else {
    return getSubtitle(TEMP_SUBTITLES[tempRange], index % 4);
  }
}
